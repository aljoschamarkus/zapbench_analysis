def _unique_int(series_like):
    """Return unique ints from a pandas Series-like (used for per-layer uniqueness)."""
    if series_like is None:
        return []
    return (
        series_like.dropna()
        .astype(int)
        .astype("int64")
        .unique()
        .tolist()
    )

def _select_top_percent_from_df(df, partner_col, top_percent):
    """
    Collapse edges from the current frontier to each partner by total weight,
    then keep the top X% of partners (by summed weight). Guarantees at least 1
    if any exist. Returns the ordered list of partner IDs (descending weight).
    """
    if df is None or df.empty:
        return []

    # Sum weights per partner, sort descending
    agg = (
        df.groupby(partner_col, as_index=False)["weight"]
          .sum()
          .sort_values("weight", ascending=False)
    )
    # compute how many to keep
    if top_percent >= 100:
        k = len(agg)
    else:
        k = max(1, int(round(len(agg) * (top_percent / 100.0))))
    top_ids = agg[partner_col].dropna().astype(int).astype("int64").head(k).tolist()
    return top_ids

def expand_partners(
    start_ids,
    depth,
    direction,
    threshold,
    *,
    top_percent=100,       # keep top X% by summed connectivity at each hop
    per_layer_unique=True, # keep layers unique (Neuroglancer-friendly)
    verbose=True
):
    """
    Expand partners over N hops with an optional top-X% filter per hop.

    - direction: 'post' (postsynaptic fan-out) or 'pre' (presynaptic fan-in)
    - threshold: keep edges with weight > threshold
    - top_percent: at each hop, collapse multiple edges to a partner by SUM(weight),
      sort desc, keep top X% (>=1 if any). These become the next-hop seeds.
    - per_layer_unique: enforce uniqueness within each layer (recommended)

    Prints per-layer stats.
    """
    if depth <= 0 or not start_ids:
        if verbose:
            print(f"depth={depth}, seeds={len(start_ids) if start_ids else 0} → nothing to do.")
        return []

    current = [int(i) for i in start_ids]
    layers = []

    for hop in range(1, depth + 1):
        if verbose:
            print(f"hop {hop}/{depth} | seeds={len(current)}")

        if not current:
            if verbose:
                print(f"hop {hop}: frontier empty — stopping.")
            break

        # fetch all adjacencies from *this* frontier
        if direction == "post":
            _, df = fetch_adjacencies(current, None)
            partner_col = "bodyId_post"
        elif direction == "pre":
            _, df = fetch_adjacencies(None, current)
            partner_col = "bodyId_pre"
        else:
            raise ValueError("direction must be 'post' or 'pre'")

        # apply edge threshold
        if df is not None and not df.empty:
            df = df[df["weight"] > threshold]
        else:
            df = df  # None or empty

        # collapse + select top percent by total weight
        next_ids = _select_top_percent_from_df(df, partner_col, top_percent)

        # enforce within-layer uniqueness (keeps predictable NG behavior)
        if per_layer_unique:
            next_ids = list(dict.fromkeys(next_ids))

        if verbose:
            kept_pct = f"{top_percent:.0f}%" if top_percent <= 100 else "100%"
            raw_count = 0 if df is None else df[partner_col].dropna().shape[0]
            unique_partners = 0 if df is None else df[partner_col].dropna().astype(int).nunique()
            print(f"hop {hop}: raw edges={raw_count}, unique partners={unique_partners}, "
                  f"kept top {kept_pct} → layer size={len(next_ids)}")

        layers.append(next_ids)
        current = next_ids  # filtered partners become seeds for next hop

        if not current:
            if verbose:
                print(f"hop {hop}: no seeds for next hop — stopping.")
            break

    return layers


